using System;
using System.Collections.Generic;

namespace Page35Drill
{
    public class Methods
    {
        public int Added(int numInput)
        {
            int addedResult = numInput + 5;
            return addedResult;
        }

        public int Subtracted(int numInput)
        {
            int subtractedResult = 100 - numInput;
            return subtractedResult;
        }

        public int Times(int numInput)
        {
            int timesResult = numInput * 1000;
            return timesResult;
        }
    }
}
